    document.addEventListener('DOMContentLoaded', function () {
    const articlePages = document.querySelectorAll('.article-page');
    const articlePagination = document.querySelectorAll('.article-g-pagination__item');

    console.log('Найдено страниц:', articlePages.length);
    console.log('Найдено кнопок:', articlePagination.length);

    articlePagination.forEach(item => {
        item.addEventListener('click', function () {
            let pageNumber = parseInt(this.dataset.page); 

            articlePages.forEach(page => { 
                page.classList.remove('active');
            });

            const targetPage = document.querySelector(
                '.article-page[data-page="' + pageNumber + '"]' 
            );

            if (targetPage) {
                targetPage.classList.add('active');
            }

            articlePagination.forEach(btn => { 
                btn.classList.remove('g-pagination__item--active');
            });

            const activeBtn = document.querySelector(
                '.article-g-pagination__item[data-page="' + pageNumber + '"]:not(.g-pagination__item--next)'
            );

            if (activeBtn) {
                activeBtn.classList.add('g-pagination__item--active');
            }

            // Прокрутка к началу страницы
            window.scrollTo({ top: 0, behavior: 'smooth' });
        });
    });
});
</script>