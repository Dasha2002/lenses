    <h1 class="service__title" >Подбор склеральных жёстких контактных линз</h1>
    <p>Склеральные контактные линзы — инновационное решение для коррекции зрения при сложных офтальмологических патологиях, таких как кератоконус, рубцовые изменения роговицы и высокая степень астигматизма. В салоне <a href="https://linz-ochki.ru/" target="_blank">«Линз-Очки»</a> в Санкт-Петербурге опытные офтальмологи проводят <strong>профессиональный подбор склеральных жёстких линз</strong>. Стоимость услуги составляет <strong>6000 ₽</strong>.</p>
    <h2>Что такое склеральные жёсткие линзы?</h2>
    <p>Склеральные линзы — это жёсткие газопроницаемые контактные линзы большого диаметра, которые полностью покрывают роговицу и опираются на склеру глаза (белочную оболочку). Между роговицей и линзой образуется резервуар жидкости, что обеспечивает комфорт ношения даже при выраженных изменениях роговицы.</p>
    <h2>Показания для подбора склеральных линз</h2>
    <ul>
        <li>Кератоконус</li>
        <li>Рубцы на роговице после травм или операций</li>
        <li>Высокий астигматизм</li>
        <li>Синдром сухого глаза</li>
        <li>Тонкая или деформированная роговица</li>
    </ul>
    <h2>Преимущества склеральных жёстких линз</h2>
    <ul>
        <li>Чёткое и стабильное зрение даже при тяжёлых нарушениях</li>
        <li>Повышенный комфорт за счёт слоя слёзной жидкости</li>
        <li>Защита роговицы от внешних воздействий</li>
        <li>Подходят для длительного ношения</li>
    </ul>
    <h2>Этапы подбора склеральных линз</h2>
    <ul>
        <li>Обследование глаз и построение карты поверхности роговицы</li>
        <li>Выбор размера и формы линзы индивидуально для каждого пациента</li>
        <li>Примерка тестовых моделей и оценка посадки, заказ линз в лаборатории</li>
        <li>Инструктаж по надеванию, снятию и уходу за склеральными линзами</li>
    </ul>
