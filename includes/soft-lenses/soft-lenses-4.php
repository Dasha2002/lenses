                <h3 class="lens-info-title">Что такое мягкие контактные линзы?</h3>
                <p class="lens-info-text">Мягкие контактные линзы изготовлены из гидрогелевых или
                    силикон-гидрогелевых материалов, которые содержат большое количество воды и обеспечивают
                    комфортное ношение. Они мягкие и гибкие, что позволяет им адаптироваться к форме глаза.</p>
                <p class="lens-info-text">Современные мягкие линзы обладают высокой кислородопроницаемостью, что
