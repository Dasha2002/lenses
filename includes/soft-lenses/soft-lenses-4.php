                    покрытия, предотвращающие накопление белков и жировых отложений.</p>
                <p class="lens-info-text">Для контроля миопии используются специальные дизайны линз, которые создают
                    контролируемую дефокусацию в периферической зоне глаза, что помогает замедлить рост глазного
                    яблока.</p>
